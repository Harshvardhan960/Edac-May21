class Pattern25
{
		public static void main(String[]args)
		{
			int t =0;
			for(int i=1;i<=5;i++)
			{
				for(int j=1; j<=i;j++)
				{
					
					System.out.print(j);
					t = t+1;
					
					
					
				}
				System.out.println(" "+t);	
				System.out.println();	
			} 
		}	
}
